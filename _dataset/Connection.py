class Connection: 

    def __init__(self,station1,station2,line,time) -> None:
        self.station1 = station1
        self.station2 = station2
        self.line = line
        self.time = time

    def get_station1(self):
        return self.station1

    def get_station2(self):
        return self.station2

    def get_line(self):
        return self.line
    
    def get_time(self):
        return self.time
        



    



        
